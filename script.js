function arbolito() {

    for (let step = 1; step <= 6; step++) {
        while (i <= 15) {
            //console.log (hojas);
            for (let s = i; s <= i; s++)
                hojas = Math.round(Math.random());
        }
        i += 3;
    }
    return hojas
}


